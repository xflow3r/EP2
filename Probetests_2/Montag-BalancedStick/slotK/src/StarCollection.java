/**
 * A collection of 'Star' objects.
 * Please, do not change this interface definition!
 */
public interface StarCollection {

    /**
     * Returns 'true' if 'this' has an element equal to the specified object 's' and 'false'
     * otherwise.
     *
     * @param s the star to check for containment, s != null.
     * @return true if 'this' has an element equal to 's', false otherwise.
     */
    boolean contains(Star s);
}
